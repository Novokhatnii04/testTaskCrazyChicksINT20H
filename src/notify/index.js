import { toast, Bounce } from "react-toastify";

export const notifySucess = (text) => {
  toast.success(`${text}`, {
    position: "top-center",
    autoClose: 4000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
    transition: Bounce,
  });
};

